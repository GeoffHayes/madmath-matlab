%**************************************************************************
% Returns the volume of a rectangular box.
%
% @param   l   The length of the rectangular box.
% @param   w   The width of the rectangular box.
% @param   h   The height of the rectangular box.
%
% @return  The volume, l*w*h.
%
% [1] Hunt, Richard A., Calculus of a Single Variable, Harper & Row
%     1998.
%**************************************************************************
% If you have any questions, comments, or find bugs, please feel free to 
% email me at geoff.hayes74@gmail.com.
%
% Geoff Hayes 2014
%**************************************************************************
function [v] = vol_rect(l,w,h)

    v = l*w*h;

end

